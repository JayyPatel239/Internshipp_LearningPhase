#for loop with lists
l =[1,3,4,6,345,22,1]
for i in l:
    print(i)
 
# for loop with tuples   
t=(1,3,4,6,345,22,1)
for i in t:
    print(i)
  
# for loop with strings  
s={'a':1,'b':2,'c':3}
for i in s:
    print(i, s[i])  # prints key and value

# for loop with strings   
s1="Hello"
for i in s1:
    print(i)  # prints each character in the string