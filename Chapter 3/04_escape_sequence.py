# \n new line
# \t tab  
# \b backspace
# \' single quote
# \" double quote
# \\ backslash

a="Hello World \n This is a new line"
print(a)
b="Hello World \t This is a tab"
print(b)    

c="Hello World \b This is a backspace"
print(c)

d=5
e=f"Hello World {d}" # f-string here we can used variables in the string
print(e)

f="Hello World \n This is a new \"line"
print(f)