# #PROPERTIES OF SETS 
# 1. Sets are unordered => Element’s order doesn’t matter 
# 2. Sets are unindexed => Cannot access elements by index 
# 3. There is no way to change items in sets. 
# 4. Sets cannot contain duplicate values.
# we can use set() to not use duplicate entries
d={11,22,33,44,44,55}
print(d, type(d)) # print the set
empty_set = set() # empty set dont use {} as it will create an empty dictionary
# empty_set = {} # empty dictionary
print(empty_set, type(empty_set)) # print the empty set

